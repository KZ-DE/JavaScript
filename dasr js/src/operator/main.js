// operator

let x = 10;
let y = 5;
let hasil;

// pertambahan
hasil = x + y;
console.log(hasil);

// perkurangan
hasil = x - y;
console.log(hasil);

// pembagian
hasil = x / y;
console.log(hasil);

// perkalian
hasil = x * y;
console.log(hasil);

// modulus
hasil = x % y;
console.log(hasil);

// increment
hasil = x++;
console.log(hasil);

// decrement
hasil = y--;
console.log(hasil);

// pangkat
hasil = x ** y;
console.log(hasil);

