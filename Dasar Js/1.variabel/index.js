// tipe data number (int dan float jadi 1)
let a, b;
a = 10;
b = 3.3;
console.log(a + b);

// notasi pada number
// hexa awalan 0x
let hex = 0xff0100;

// binary awalan 0b
let bin = 0b010001;

// octal
let oxtal = 0o10;

console.log(hex, bin, oxtal);

// tipe data boolean
let bool = true;
console.log(bool);

// tipe data string
let kalimat = "Hello Word";
console.log(kalimat);
