let a = true;
let b = false;
let hasil;

// and
hasil = a && b;
console.log(hasil);
hasil = a && a;
console.log(hasil);
hasil = b && a;
console.log(hasil);
hasil = b && b;
console.log(hasil);

console.log("\n");

// or
hasil = a || b;
console.log(hasil);
hasil = a || a;
console.log(hasil);
hasil = b || a;
console.log(hasil);
hasil = b || b;
console.log(hasil);

console.log("\n");

// not
hasil = !a;
console.log(hasil);
hasil = !a;
console.log(hasil);
hasil = !b;
console.log(hasil);
hasil = !b;
console.log(hasil);
