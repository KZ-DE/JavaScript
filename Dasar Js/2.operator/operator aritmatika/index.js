let a = 10;
let b = 3;
let hasil;

// tambah
hasil = a * b;
console.log(hasil);

// kurang
hasil = a - b;
console.log(hasil);

// kali
hasil = a * b;
console.log(hasil);

// bagi
hasil = a / b;
console.log(hasil);

// pangkat
hasil = a ** b;
console.log(hasil);

// modulus
hasil = a % b;
console.log(hasil);
